# Имеется модуль алгоритма RSA N
# Требуется найти простые множители p и q, такие что p * q = N
def sieve_of_eratosthenes(limit):
    """
    Возвращает список всех простых чисел от 2 до limit включительно,
    используя алгоритм "Решето Эратосфена".
    """

    # Шаг 1: Создаем булевый массив размером (limit + 1),
    # изначально считая все числа простыми (True)
    is_prime = [True] * (limit + 1)

    # 0 и 1 не являются простыми числами по определению
    is_prime[0] = False
    is_prime[1] = False

    # Шаг 2: Основной цикл решета
    # Достаточно проверять числа p, квадрат которых не превышает limit.
    # Если у числа есть делитель, то хотя бы один из них <= sqrt(limit)
    for p in range(2, int(limit**0.5) + 1):
        # Если is_prime[p] не изменился (остался True), значит p — простое число
        if is_prime[p]:
            # Вычеркиваем все кратные p, начиная с p^2.
            # Почему с p^2? Потому что меньшие кратные (2p, 3p и т.д.)
            # уже были вычеркнуты на предыдущих шагах меньшими простыми числами.
            # Шаг цикла равен p, чтобы брать только кратные числа.
            for i in range(p * p, limit + 1, p):
                is_prime[i] = False

    # Шаг 3: Собираем все индексы, которые остались True, в список
    # enumerate возвращает пары (индекс, значение), мы берем только индекс, где значение True
    primes = [i for i, prime in enumerate(is_prime) if prime]

    return primes

n = 2478489083359787 # Модуль алгоритма N
print('Модуль алгоритма N = ', n)
s = sieve_of_eratosthenes(10**9)
print('Найдено ', len(s), ' простых чисел!')
for i in range(n):
    for p in s:
        q = n / p
        if q.is_integer():
            print('p = ', p, '  q = ', int(q), '   p * q = ', int(p * q))
            exit()
