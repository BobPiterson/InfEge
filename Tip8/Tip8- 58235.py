# в записи которых сумма первой и последней цифры строго больше цифры стоящей по середине?

# Два варианта:
# Вариант 1
k = 0
for a in range(1, 4):
    for b in range(0, 4):
        for c in range(0, 4):
            if (a + c) > b:
                k += 1
print('Ответ 1 варианта =',k)

