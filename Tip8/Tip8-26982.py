for i in range(100000, 1000000):
    # разобьем 6-ти значное число на разряды
    r1 = i % 10
    r2 = i % 100 // 10
    r3 = i % 1000 // 100
    r4 = i % 10000 // 1000
    r5 = i % 100000 // 10000
    r6 = i // 100000
