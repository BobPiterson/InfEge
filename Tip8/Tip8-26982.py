    set1.add(r1)
    set1.add(r2)
    set1.add(r3)
    set1.add(r4)
    set1.add(r5)
    set1.add(r6)
    # проверим все требуемые условия
    if (i % 5 == 0) and (len(set1) == 6):
        if not ((r1 % 2 == 0) and (r2 % 2 == 0) or (r2 % 2 == 0) and (r3 % 2 == 0) or \
        (r3 % 2 == 0) and (r4 % 2 == 0) or (r4 % 2 == 0) and (r5 % 2 == 0) or (r5 % 2 == 0) and (r6 % 2 == 0)):
            # рядом не стоят две четные цифры 
            if not((r1 % 2 == 1) and (r2 % 2 == 1) or (r2 % 2 == 1) and (r3 % 2 == 1) or \
                (r3 % 2 == 1) and (r4 % 2 == 1) or (r4 % 2 == 1) and (r5 % 2 == 1) or (r5 % 2 == 1) and (r6 % 2 == 1)):
                # рядом не стоят две нечетные цифры 
                count += 1
